package Loops;

public class Table {
    public static void main(String[] args){
        for(int i=1; i<=10; i++){
            System.out.println("17 * "+i+" = "+17*i);
        }

        for(int i=19; i<=190; i=i+19){
            System.out.print(i+" ");
        }
        System.out.println("");
        for(int i=1; i<=100; i=i+2){
            System.out.print(i+" ");
        }

    }
}
